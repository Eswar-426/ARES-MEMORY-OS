/// A mock provider for fast infrastructure testing.
pub struct MockAgentProvider {
    pub mock_delay_ms: u64,
}

impl Default for MockAgentProvider {
    fn default() -> Self {
        Self::new()
    }
}

impl MockAgentProvider {
    pub fn new() -> Self {
        Self { mock_delay_ms: 10 }
    }
}

#[async_trait]
impl AgentProvider for MockAgentProvider {
    async fn generate(
        &self,
        _system_prompt: &str,
        _user_prompt: &str,
        _history: &[serde_json::Value],
        available_tools: &[serde_json::Value],
    ) -> anyhow::Result<(String, Vec<ToolCall>, ProviderUsage)> {
        tokio::time::sleep(std::time::Duration::from_millis(self.mock_delay_ms)).await;

        let usage = ProviderUsage {
            input_tokens: 1500,
            output_tokens: 50,
            total_tokens: 1550,
            cost_usd: 0.0015,
        };

        // For mock, just return a fake tool call or success response.
        // We'll simulate a read_file if it's available.
        if available_tools.iter().any(|t| t["name"] == "read_file") {
            let tc = ToolCall {
                name: "read_file".into(),
                args: serde_json::json!({ "path": "src/main.rs" }),
            };
            return Ok(("I will check the main file.".into(), vec![tc], usage));
        }

        Ok(("Task completed. Mock success.".into(), vec![], usage))
    }
}
