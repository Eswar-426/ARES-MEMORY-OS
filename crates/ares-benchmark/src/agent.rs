use async_trait::async_trait;
use serde::{Deserialize, Serialize};

/// Identifies the type of agent being benchmarked.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum AgentType {
    /// Agent A: Files Only (No ARES context)
    Baseline,
    /// Agent B: Files + ARES MCP Tools
    Ares,
    /// Agent C: Entire Repo Summary in Context
    ContextDump,
    /// Agent D: Context Dump + ARES MCP Tools
    ContextDumpAndAres,
}

impl AgentType {
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::Baseline => "Baseline",
            Self::Ares => "ARES",
            Self::ContextDump => "Context Dump",
            Self::ContextDumpAndAres => "Context Dump + ARES",
        }
    }
}

/// Metrics collected during a single agent's run of a task.
#[derive(Debug, Clone, Serialize, Deserialize, Default)]
pub struct BenchmarkMetrics {
    /// Latency / Total time elapsed in seconds
    pub time_elapsed_secs: f64,
    /// Total tokens consumed (prompt + completion)
    pub total_tokens: usize,
    /// Input tokens
    pub input_tokens: usize,
    /// Output tokens
    pub output_tokens: usize,
    /// Calculated dollar cost
    pub provider_cost_usd: f64,
    /// Search Depth: Number of file read/search tools invoked before the first mutating action
    pub search_depth: usize,
    /// Success Rate (Hybrid Score: 0-100)
    pub success_score: f64,
    /// Did the agent attempt a known bug?
    pub repeated_failure: bool,
    /// Assessment of the agent's plan/execution order
    pub planning_quality_score: f64,
}

/// A standard tool call generated by the agent.
#[derive(Debug, Clone)]
pub struct ToolCall {
    pub name: String,
    pub args: serde_json::Value,
}

/// Result of a tool call
#[derive(Debug, Clone)]
pub struct ToolResult {
    pub output: String,
    pub is_error: bool,
}

/// Represents the interface to any LLM provider.
#[async_trait]
pub trait AgentProvider: Send + Sync {
    /// Provide a prompt and a list of tool schemas, returns a text response or a set of tool calls.
    async fn generate(
        &self,
        system_prompt: &str,
        user_prompt: &str,
        history: &[serde_json::Value],
        available_tools: &[serde_json::Value],
    ) -> anyhow::Result<(String, Vec<ToolCall>, ProviderUsage)>;
}

/// Usage stats returned from the provider after a generation call
#[derive(Debug, Clone, Default)]
pub struct ProviderUsage {
    pub input_tokens: usize,
    pub output_tokens: usize,
    pub total_tokens: usize,
    pub cost_usd: f64,
}

